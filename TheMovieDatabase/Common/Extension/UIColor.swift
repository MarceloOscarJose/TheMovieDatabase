import UIKit

extension UIColor {

    class var shadowColor: UIColor {
        return UIColor(red: 218.0 / 255.0, green: 218.0 / 255.0, blue: 218.0 / 255.0, alpha: 1.0)
    }

    class var ligthBlue: UIColor {
        return UIColor(red: 0.25, green: 0.72, blue: 0.85, alpha: 1.0)
    }
}
